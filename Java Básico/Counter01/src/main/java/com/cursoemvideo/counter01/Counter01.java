public class Counter01 {

    public static void main(String[] args) {
        int sc = 0;
        while (sc < 4){
            System.out.println("Preparing Somersault #" + sc);
            System.out.println("Doing Somersault #" + sc);
            System.out.println("Somersault #" + sc + " done");
            System.out.println("=============================");
            sc++;
        }
    }
}
